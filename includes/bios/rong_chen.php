				<p>Rong Chen, MD PhD, is the Chief Medical Informatics Officer and the head of Medical Informatics Group at <a href="http://www.openehr.org/industry_partners/cambio_healthcare_systems">Cambio Healthcare Systems</a>, Sweden, where he is responsible for the research and development in clinical decision support and knowledge management.</p>
				<p>Rong holds a PhD degree in health informatics on the subject of EHR semantic interoperability from Linköping University, Sweden. He is an associated researcher at the Health Informatics Centre at Karolinska Institutet, where he lectures and supervises PhD and master students.</p>
				<p>Rong has contributed to several core openEHR design specifications, and has for many years led the <a href="https://openehr.atlassian.net/wiki/display/projects/Java+Project+Download">Java Reference Implementation of openEHR</a>. Rong is the lead author of <a href="https://github.com/openEHR/specifications/blob/master/proposals/GDL/GuideDefinitionLanguage_v0.9.pdf?raw=true">Guideline Definition Language (GDL)</a>, which is designed to express clinical logic for scalable decision support with openEHR underpinning.</p>
